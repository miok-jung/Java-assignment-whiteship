package study3;

public class BitLogicExample {
    // 비트 논리 연산자
    public static void main2(String[] args) {
        System.out.println("45 & 25 = " + (45 & 25)); // 9
        System.out.println("45 | 25 = " + (45 | 25)); // 61
        System.out.println("45 ^ 25 = " + (45 ^ 25)); // 52
        System.out.println("~45 = " + (~45)); // -46
    }
}
